                <p>A los dirigentes europeos se les debería caer la cara de vergüenza por la ínfima cantidad de refugiados de Siria que sus Estados están dispuestos a reasentar; así lo ha afirmado Amnistía Internacional.</p>
                <p>En un documento que ha publicado hoy, la organización especifica que los Estados miembros de la Unión Europea (UE) sólo se han ofrecido a abrir sus puertas a unos 12.000 de entre los refugiados más vulnerables de Siria: es decir, tan sólo el 0,5 por ciento de los 2,3 millones de personas que han huido del país.</p>
                <p>"La UE ha hecho una lamentable dejación de su parte de responsabilidad en proporcionar un lugar seguro a unos refugiados que lo han perdido todo excepto la vida. La cantidad de refugiados que están dispuestos a reasentar es realmente ridícula. Todos los dirigentes europeos sin excepción deben avergonzarse”, ha dicho Salil Shetty, secretario general de Amnistía Internacional.</p>
                <p>>Nicosia, capital de un Estado miembro de la UE, Chipre, está situada a escasos 300 kilómetros de Damasco; sin embargo, colectivamente, los Estados miembros de la UE se han comprometido a reasentar un porcentaje minúsculo de los refugiados más vulnerables de Siria. En su documento, Amnistía Internacional desglosa así </p>
                <ol>
                    <li>Sólo 10 Estados miembros de la UE han ofrecido plazas de reasentamiento o admisión por razones humanitarias a refugiados procedentes de Siria.</li>
                    <li>Alemania ha sido, con diferencia, el Estado más generoso al comprometerse a acoger 10.000 refugiados, es decir, el 80 por ciento del número total de refugiados que la UE se ha comprometido a acoger.</li>
                    <li>Excluyendo Alemania, los 27 Estados miembros de la UE restantes se han ofrecido a acoger solamente 2.340 refugiados de Siria.</li>
                    <li>Francia sólo ha ofrecido 500 plazas, es decir, el 0,02 por ciento del número total de personas que han huido de Siria.</li>
                    <li>España ha aceptado acoger sólo 30, el 0,001 por ciento de los refugiados de Siria.</li>
                    <li>Dieciocho Estados miembros de la UE –entre ellos Reino Unido e Italia– no han ofrecido una sola plaza.</li>
                </ol>
                <p>Se acerca el invierno y las condiciones de vida para los 2,2 millones de personas que han huido de Siria a países vecinos están empeorando por momentos.</p>
                <p>En vista de que los Estados miembros de la UE sólo han ofrecido 12.000 plazas para reasentamiento o admisión por motivos humanitarios de los refugiados más vulnerables, decenas de miles de sirios arriesgan la vida y la integridad física en difíciles travesías, en barco o por tierra, buscando seguridad y protección en Europa.</p>
                <p>La investigación de Amnistía Internacional revela que para ello tienen que atravesar las barricadas de la Fortaleza Europa. Muchos son víctimas de violentas operaciones de devolución ilegal a manos de la policía y los guardacostas, o son detenidos y privados de libertad durante semanas en condiciones deplorables.</p>
                <h6>El viaje a Italia por mar</h6>
                <p>Cientos de personas mueren cada año intentando cruzar el mar Mediterráneo. Al menos 650 refugiados y migrantes, según cálculos, perdieron la vida en octubre cuando tres barcos que intentaban llegar a Europa desde el Norte de África naufragaron.</p>
                <p>Según informes, más de 10.000 refugiados procedentes de Siria han llegado a la costa de Italia en los 10 primeros meses de 2013.</p>
                <p>El documento de Amnistía Internacional incluye relatos de primera mano de personas que intentaron llegar a Europa por mar.</p>
                <p>Un joven de 17 años procedente de Damasco, Awad, explica cómo pudo escapar por una ventana de un barco que se estaba hundiendo y nadar hasta la superficie. Al parecer, había 400 personas a bordo. Vio a personas aferrándose a cadáveres y restos del naufragio para mantenerse a flote, y a otras peleándose por chalecos salvavidas. Awad perdió a su madre y a otros miembros de su familia.</p>
                <p>“No tengo ni idea de dónde está mi familia [...] Antes tenía ambiciones, pero desde que he perdido a mi madre ya no quiero nada, sólo busco estabilidad, todo lo demás es secundario.”</p>
                <p>Otro muchacho de Siria perdió a su padre y a un hermano de nueve años en el accidente.</p>
                <p>“Lo que viví no sólo destruyó mis sueños, sino también los sueños de mi familia. Estoy completamente destrozado.”</p>
